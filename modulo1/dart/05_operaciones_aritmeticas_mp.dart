void main() {
  print("Cálculos de Servicios");
  final double costoBase = 12.5;
  final double adicional = 5;
  print("Total  $costoBase + $adicional = ${costoBase + adicional}");
  print("Diferencia  $costoBase - $adicional = ${costoBase - adicional}");
  print("Paquete completo  $costoBase * $adicional = ${costoBase * adicional}");
  print("Costos unitarios  $costoBase / $adicional = ${costoBase / adicional}");
}